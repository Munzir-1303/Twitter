let likeclick = document.querySelector('.hero__center--chat2img2link2222');
let likewhite = document.querySelector('.hero__center--chat2img2222');
let likered = document.querySelector('.hero__center--chat2img3');
let likeresult = document.querySelector('.hero__center--chat2img2spanlike');
let clicked = false;

likeclick.addEventListener('click', () => {
    if(!clicked){
        clicked = true;
        likewhite.style.display = 'none';
        likered.style.display = 'block';
        likeresult.textContent++;
    }else{
        clicked = false;
        likewhite.style.display = 'block';
        likered.style.display = 'none';
        likeresult.textContent--;
    }
});

